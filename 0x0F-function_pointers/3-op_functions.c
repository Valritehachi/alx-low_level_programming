/**
 * op_add - add
 * @a: first int
 * @b: second int
 * Return: Always 0.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub
 * @a: first int
 * @b: second int
 * Return: Always 0.
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - mul
 * @a: first int
 * @b: second int
 * Return: Always 0.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div
 * @a: first int
 * @b: second int
 * Return: Always 0.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod
 * @a: first int
 * @b: second int
 * Return: Always 0.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
